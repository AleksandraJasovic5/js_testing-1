class Maths {
    static add(num1, num2) {
        return num1 + num2;
    }

    static sub(num1, num2) {
        return num1 - num2;
    }
    static divide (num1, num2) {
        return num1 / num2;
    }
    static multiply(num1, num2, num3) {
        return num1 * num2 * num3;
    }
}

module.exports = Maths;
